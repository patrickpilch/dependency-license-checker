package io.github.patrickpilch.dependencylicensechecker;

/**
 * Value object for user provided artifacts that are meant to be excluded from license checking.
 * Must be a public class so Mojo can instantiate them. Private fields have no setters because
 * Mojo will populate them with reflection.
 */
@SuppressWarnings({"WeakerAccess", "unused"})
public class ArtifactExclusion {
    private String groupId;
    private String artifactId;
    private String version;

    public ArtifactExclusion() {
        // Required for Mojo to instantiate this when parsing the xml configuration
    }

    public ArtifactExclusion(String groupId, String artifactId, String version) {
        this.groupId = groupId;
        this.artifactId = artifactId;
        this.version = version;
    }

    String getGroupId() {
        return groupId;
    }

    String getArtifactId() {
        return artifactId;
    }

    String getVersion() {
        return version;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        ArtifactExclusion that = (ArtifactExclusion) o;

        if (groupId != null ? !groupId.equals(that.groupId) : that.groupId != null) return false;
        if (artifactId != null ? !artifactId.equals(that.artifactId) : that.artifactId != null) return false;
        return version != null ? version.equals(that.version) : that.version == null;

    }

    @Override
    public int hashCode() {
        int result = groupId != null ? groupId.hashCode() : 0;
        result = 31 * result + (artifactId != null ? artifactId.hashCode() : 0);
        result = 31 * result + (version != null ? version.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "ArtifactExclusion{" +
                "groupId='" + groupId + '\'' +
                ", artifactId='" + artifactId + '\'' +
                ", version='" + version + '\'' +
                '}';
    }
}
